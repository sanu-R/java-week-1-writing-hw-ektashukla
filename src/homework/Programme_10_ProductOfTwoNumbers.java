package homework;

/**
 * 10. Write a Java programme that takes two numbers as input and displays the product
 * of the two numbers.
 * Test Data:
 * Input first number: 25
 * Input second number : 5
 * Expected Output : 25 x 5 = 125
 */
public class Programme_10_ProductOfTwoNumbers {
    public static void main(String[] args) {
        //First Number declaration
        int fistNumber = 25;
        // second Number declaration
        int secondNumber = 5;
        int produced = fistNumber * secondNumber;
        //Expected output
        System.out.println(fistNumber + " x " + secondNumber + " = " + produced);

    }
}
